export const getRandomActivity = (excludeIds = []) => {
  const availableActivities = activities.filter(activity => !excludeIds.includes(activity.id));
  if (availableActivities.length === 0) return null;
  
  const randomIndex = Math.floor(Math.random() * availableActivities.length);
  return availableActivities[randomIndex];
};

export const getActivityById = (id) => {
  return activities.find(activity => activity.id === id);
};

export const getAllCategories = () => {
  return [...new Set(activities.map(activity => activity.category))];
};
